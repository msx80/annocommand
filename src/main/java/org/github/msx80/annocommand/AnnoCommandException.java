package org.github.msx80.annocommand;

public class AnnoCommandException extends RuntimeException {

	private static final long serialVersionUID = 856330720119399L;

	public AnnoCommandException() {
		super();
	}

	public AnnoCommandException(String message, Exception cause) {
		super(message, cause);
	}

	public AnnoCommandException(String message) {
		super(message);
	}

	public AnnoCommandException(Exception cause) {
		super(cause);
	}

	
	
	
}
